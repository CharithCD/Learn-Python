def main():
    addTwoNumbers(x, y)
    subtractTwoNumbers(x, y)
    multiplyTwoNumbers(x, y)
    divideTwoNumbers(x, y)

def addTwoNumbers(x, y):
    return x + y

def subtractTwoNumbers(x, y):
    return x - y

def multiplyTwoNumbers(x, y):
    return x * y

def divideTwoNumbers(x, y):
    return x / y

if __name__ == "__main__":
    main()